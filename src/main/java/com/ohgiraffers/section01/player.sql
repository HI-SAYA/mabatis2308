COMMENT ON COLUMN TBL_TEAM.LEAGUE_NAME IS '리그명';
COMMENT ON TABLE TBL_TEAM IS '구단정보';

COMMENT ON COLUMN TBL_PLAYER.PLAYER_CODE IS '선수코드';
COMMENT ON COLUMN TBL_PLAYER.PLAYER_NO IS '선수번호';
COMMENT ON COLUMN TBL_PLAYER.PLAYER_NAME IS '선수이름';
COMMENT ON COLUMN TBL_PLAYER.TEAM_NAME IS '구단명';
COMMENT ON COLUMN TBL_PLAYER.TEAM_CODE IS '구단번호';
COMMENT ON COLUMN TBL_PLAYER.LEAGUE_NAME IS '리그명';
COMMENT ON TABLE TBL_PLAYER IS '선수정보';


