

DROP TABLE TBL_TEAM CASCADE CONSTRAINTS;
DROP TABLE TBL_PLAYER CASCADE CONSTRAINTS;

DROP SEQUENCE SEQ_TEAM_CODE;
DROP SEQUENCE SEQ_PLAYER_CODE;

CREATE TABLE TBL_TEAM
(
    TEAM_CODE    NUMBER CONSTRAINT PK_TEAM_CODE PRIMARY KEY NOT NULL, -- PK
    TEAM_NAME    VARCHAR2(30) NOT NULL,
    LEAGUE_CODE  NUMBER NOT NULL,
    LEAGUE_NAME  VARCHAR2(30) NOT NULL
);

CREATE TABLE TBL_PLAYER
(
    PLAYER_CODE  NUMBER CONSTRAINT PK_PLAYER_CODE PRIMARY KEY NOT NULL, -- PK
    PLAYER_NO    NUMBER NOT NULL,
    PLAYER_NAME  VARCHAR2(30) NOT NULL,
    TEAM_CODE    NUMBER NOT NULL, -- FK
    TEAM_NAME    VARCHAR2(30) NOT NULL,
    LEAGUE_NAME  VARCHAR2(30) NOT NULL,

    CONSTRAINT FK_TEAM_CODE FOREIGN KEY(TEAM_CODE) REFERENCES TBL_TEAM(TEAM_CODE)
);

CREATE SEQUENCE SEQ_TEAM_CODE;
CREATE SEQUENCE SEQ_PLAYER_CODE;

COMMENT ON COLUMN TBL_TEAM.TEAM_CODE IS '구단번호';
COMMENT ON COLUMN TBL_TEAM.TEAM_NAME IS '구단명';
